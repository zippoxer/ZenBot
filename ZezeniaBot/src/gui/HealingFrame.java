    private void init() {
        //Init the spellsModel.
        for (String s : Spells.getSpellNames()) {
            spellsModel.addElement(s);
        }
        spellCombo.setSelectedIndex(0);

        unpackSettings();
    }

    /**
     * Translates UserSettings.Healing into this frame.
     */
    public void packSettings() {
        UserSettings.Healing stg = MainFrame.getInstance().settings.healing;
        stg.spellName = spellCombo.getSelectedItem().toString();
        stg.spellHealth = UserSettings.Percentable.parse(spellHealth.getText());
        stg.spellMana = UserSettings.Percentable.parse(spellMana.getText());
        stg.hpPotHealth = UserSettings.Percentable.parse(hppotHealth.getText());
        stg.hpPotType = hppotType.getSelectedIndex();
        stg.mpPotMana = UserSettings.Percentable.parse(mppotMana.getText());
        stg.mpPotType = (int) mppotType.getSelectedIndex();
    }

    /**
     * Translates this frame into UserSettings.Healing.
     */
    public void unpackSettings() {
        UserSettings.Healing set = MainFrame.getInstance().settings.healing;
        spellCombo.setSelectedItem(set.spellName);
        spellHealth.setText(set.spellHealth.toString());
        spellMana.setText(set.spellMana.toString());
        hppotHealth.setText(set.hpPotHealth.toString());
        hppotType.setSelectedItem(set.hpPotType);
        mppotMana.setText(set.mpPotMana.toString());
        mppotType.setSelectedItem(set.mpPotType);
    }
    
    public void updatePotionsLeft(int health, int mana) {
        potionsLeftLabel.setText(health + " health potions left, " + mana + " mana potions left");
    }


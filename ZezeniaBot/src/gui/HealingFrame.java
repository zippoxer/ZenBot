    private void enableButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_enableButtonMouseClicked
        packSettings();
        if (enableButton.getText().contains("Enable")) {
            enableButton.setText("Disable");
            healer = new Healer(MainFrame.getInstance().settings.healing);
            healer.start();
        } else {
            enableButton.setText("Enable");
            healer.interrupt();
            healer = null;
        }
        // TODO: start the healer.
    }//GEN-LAST:event_enableButtonMouseClicked

